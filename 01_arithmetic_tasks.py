# 2.Сумма цифр числа: Напиши программу, которая принимает на вход целое число и вычисляет сумму его цифр.
input_num = input("Введите число: ")
result = 0
if not input_num:
    print("Не может быть пустого значения")

for i in input_num:
    result += int(i)

print("Сумма числа: ", result)

