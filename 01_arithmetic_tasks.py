# 5. Остаток от деления: Напиши программу, которая запрашивает у пользователя два числа и выводит результат целочисленного деления и остаток от деления первого числа на второе.
num_first = int(input("Введите первое число: "))
num_second = int(input("Введите второе число: "))

if num_second == 0:
    print("Деление на 0 невозможно")
else:
    cel_result = num_first // num_second
    ost_result = num_first % num_second

print("результат целочисленного деления: ", cel_result)
print("остаток от деления первого числа на второе: ", ost_result)

